  /**
   * Display a listing of the resource.
   * @mixin Eloquent
   * @return \Illuminate\Http\Response
   */
  public function UserManagement()
  {
    $users = User::all();
    $totalUsers = $users->count();
    $totalVerifiedUsers = User::whereNotNull('email_verified_at')->get()->count();
    $totalNotVerifiedUsers = User::whereNull('email_verified_at')->get()->count();
    $totalUniqueUsers = $users->unique(['email']);
    $totalDuplicateUsers = $users->diff($totalUniqueUsers)->count();

    return view('admin.user-management', [
      'users' => $users,
      'totalUsers' => $totalUsers,
      'totalVerifiedUsers' => $totalVerifiedUsers,
      'totalNotVerifiedUsers' => $totalNotVerifiedUsers,
      'totalDuplicateUsers' => $totalDuplicateUsers,
    ]);
  }

  /**
   * Display a listing of the resource.
   * @mixin Eloquent
   * @return \Illuminate\Http\JsonResponse
   * index fn does not have a Request parameter, we have added this in order to fetch details like pagination
   */
  public function index(Request $request)
  {
    $columns = [
      1 => 'id',
      2 => 'name',
      3 => 'email',
      4 => 'email_verified_at',
    ];

    $users = User::all();
    $totalData = User::count();
    $totalFiltered = $totalData;
    Debugbar::info($request->input('order'));

    $limit = $request->input('length');
    $start = $request->input('start');
    $order = $columns[$request->input('order.0.column')];
    $dir = $request->input('order.0.dir');

    if (empty($request->input('search.value'))) {
      $users = User::offset($start)
        ->limit($limit)
        ->orderBy($order, $dir)
        ->get();
    } else {
      $search = $request->input('search.value');

      $users = User::where('id', 'LIKE', "%{$search}%")
        ->orWhere('name', 'LIKE', "%{$search}%")
        ->orWhere('email', 'LIKE', "%{$search}%")
        ->offset($start)
        ->limit($limit)
        ->orderBy($order, $dir)
        ->get();

      $totalFiltered = User::where('id', 'LIKE', "%{$search}%")
        ->orWhere('name', 'LIKE', "%{$search}%")
        ->orWhere('email', 'LIKE', "%{$search}%")
        ->count();
    }


    $data = [];

    if (!empty($users)) {
      // providing a dummy id instead of database ids
      $ids = $start;

      foreach ($users as $user) {
        $nestedData['id'] = $user->id;
        $nestedData['fake_id'] = ++$ids;
        $nestedData['name'] = $user->name;
        $nestedData['email'] = $user->email;
        $nestedData['email_verified_at'] = $user->email_verified_at;

        $data[] = $nestedData;
      }
    }

    if ($data) {
      return response()->json([
        'draw' => intval($request->input('draw')),
        'recordsTotal' => intval($totalData),
        'recordsFiltered' => intval($totalFiltered),
        'code' => 200,
        'data' => $data,
      ]);
    } else {
      return response()->json([
        'message' => 'Internal Server Error',
        'code' => 500,
        'data' => [],
      ]);
    }
  }

